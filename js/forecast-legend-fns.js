const dictToLegend = (dict, title, subtitle) => {
  let legendHTML = ''
  for (let key in dict) {
    legendHTML += `<div><span style="background-color: ${dict[key]['color']}"></span>` + dict[key]['label'] + '</div>'
  }
  document.getElementById('legend-items').innerHTML = legendHTML
  document.getElementById('legend-title-text').innerText = title
  document.getElementById('legend-subtitle-text').innerText = subtitle
}

